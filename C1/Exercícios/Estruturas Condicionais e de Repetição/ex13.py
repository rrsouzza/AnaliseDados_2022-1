diaInput = int(input("Insira um número do dia da semana: "))
if diaInput == 1:
  print("1 - Domingo")
elif diaInput == 2:
  print("2 - Segunda-feira")
elif diaInput == 3:
  print("3 - Terça-feira")
elif diaInput == 4:
  print("4 - Quarta-feira")
elif diaInput == 5:
  print("5 - Quinta-feira")
elif diaInput == 6:
  print("6 - Sexta-feira")
elif diaInput == 7:
  print("7 - Sábado")
else:
  print("Valor inválido!")
